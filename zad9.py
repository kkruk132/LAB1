#Wczytywanie zmiennych od użytkownika
a = float(input("Podaj pierwszą liczbę:"))
b = float(input("Podaj drugą liczbę:"))

 #działania na podanych zmiennych
dodawanie = a + b
odejmowanie = a - b
mnozenie = a * b
dzielenie = a / b
potegowanie = a ** b

#drukowanie wyników
print("Wynik dodawania podanych liczb to:",dodawanie)
print("Wynik odejmowania podanych liczb to:",odejmowanie)
print("Wynik mnożenia podanych liczb to:",mnozenie)
print("Wynik dzielenia podanych liczb to:",dzielenie)
print("Wynik potęgowania podanych liczb to:",potegowanie)